static int panel_send_dsi_cmds(struct mipi_dsi_device *dsi, u8 *data, int len)
{
	int i, delay, cmd_len, ret;

	for (i = 0; i < len; i += (cmd_len + 3)) {
		delay = data[i + 1];
		cmd_len = data[i + 2];
		if (cmd_len + i + 3 > len) {
			print_hex_dump("cmd overflow, cmd: ", DUMP_PREFIX_NONE,
			               16, 1, &data[i], len - i, false);
			return -EINVAL;
		}

		pr_info("%s delay = %d, cmd_len = %d\n", __func__, delay, cmd_len);

		switch (data[i]) {
		case MIPI_DSI_DCS_SHORT_WRITE:
		case MIPI_DSI_DCS_SHORT_WRITE_PARAM:
		case MIPI_DSI_DCS_LONG_WRITE:
			ret = mipi_dsi_dcs_write_buffer(dsi, &data[i + 3], cmd_len);
			if (ret < 0) {
				print_hex_dump("dcs send failed, cmd: ", DUMP_PREFIX_NONE,
				               16, 1, &data[i], cmd_len + 3, false);
				return ret;
			}
			break;

		case MIPI_DSI_GENERIC_SHORT_WRITE_0_PARAM:
		case MIPI_DSI_GENERIC_SHORT_WRITE_1_PARAM:
		case MIPI_DSI_GENERIC_SHORT_WRITE_2_PARAM:
		case MIPI_DSI_GENERIC_LONG_WRITE:
			ret = mipi_dsi_generic_write(dsi, &data[i + 3], cmd_len);
			if (ret < 0) {
				print_hex_dump("generic send failed, cmd: ", DUMP_PREFIX_NONE,
				               16, 1, &data[i], cmd_len + 3, false);
				return ret;
			}
			break;

		default:
			pr_err("%s unsupported data type, type = 0x%x\n", __func__, data[i]);
			return -EINVAL;
		}

		print_hex_dump("send success, cmd: ", DUMP_PREFIX_NONE,
		               16, 1, &data[i], cmd_len + 3, false);

		if (delay)
			msleep(delay);
	}

	return 0;
}

static int simple_panel_init(struct simple_panel_cfg *panel_cfg)
{
	int ret = 0, gpio;
	struct dsi_panel_cfg *dp_cfg = &panel_cfg->dp_cfg;

	if (PANEL_GPIO_VALID(panel_cfg->enable_gpio)) {
		gpio = PANEL_GPIO_GET(panel_cfg->enable_gpio);

		ret = gpio_request(gpio, "panel-enable");
		if(ret < 0) {
			VO_ERROR("request enable-gpio(%d) failed, ret:%d\n", gpio, ret);
			goto exit;
		}

		ret = gpio_direction_output(gpio, 1);
		if(ret < 0) {
			VO_ERROR("set enable-gpio(%d) direction failed, ret:%d\n", gpio, ret);
			goto exit;
		}
	}

	if (PANEL_GPIO_VALID(panel_cfg->reset_gpio)) {
		gpio = PANEL_GPIO_GET(panel_cfg->reset_gpio);

		ret = gpio_request(gpio, "panel-reset");
		if(ret < 0) {
			VO_ERROR("request reset-gpio(%d) failed, ret:%d\n", gpio, ret);
			goto exit;
		}

		ret = gpio_direction_output(gpio, 1);
		if(ret < 0) {
			VO_ERROR("set reset-gpio(%d) direction failed, ret:%d\n", gpio, ret);
			goto exit;
		}

		gpio_set_value(gpio, 0);
		mdelay(10);
		gpio_set_value(gpio, 1);
		mdelay(panel_cfg->reset_delay_ms);
	}

	if ((panel_cfg->type == PANEL_TYPE_MIPI) && dp_cfg->dsi_dev)
		ret = panel_send_dsi_cmds(dp_cfg->dsi_dev, dp_cfg->init_seq, dp_cfg->num_init_seqs);

exit:
	VO_INFO("panel init %s\n", ret ? "failed" : "success");

	return ret;
}

